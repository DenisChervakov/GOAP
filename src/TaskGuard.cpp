#	include "GOAP/TaskGuard.h"
#	include "GOAP/GuardProvider.h"

namespace GOAP
{
	//////////////////////////////////////////////////////////////////////////
	TaskGuard::TaskGuard(const GuardProviderPtr & _begin, const GuardProviderPtr & _end)
		: m_begin(_begin)
		, m_end(_end)
	{}
	//////////////////////////////////////////////////////////////////////////
	TaskGuard::~TaskGuard()
	{}
	//////////////////////////////////////////////////////////////////////////
	bool TaskGuard::onRun()
	{		
		m_begin->onGuard();

		return false;
	}
	//////////////////////////////////////////////////////////////////////////
	void TaskGuard::onCancel()
	{
		m_end->onGuard();
	}
}
