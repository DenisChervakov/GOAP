#	include "TaskPrint.h"

//////////////////////////////////////////////////////////////////////////
TaskPrint::TaskPrint( const std::string & _message )
	: m_message( _message )
{
}
//////////////////////////////////////////////////////////////////////////
TaskPrint::~TaskPrint()
{
}
//////////////////////////////////////////////////////////////////////////
bool TaskPrint::onRun()
{
	printf( "%s\n"
		, m_message.c_str()
		);

	return true;
}