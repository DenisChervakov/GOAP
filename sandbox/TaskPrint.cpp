#	include "TaskPrint.h"

//////////////////////////////////////////////////////////////////////////
TaskPrint::TaskPrint( const std::string & _message )
    : GOAP::Task( GOAP::TASK_EVENT_RUN )
	, m_message( _message )
{
}
//////////////////////////////////////////////////////////////////////////
TaskPrint::~TaskPrint()
{
}
//////////////////////////////////////////////////////////////////////////
bool TaskPrint::_onRun()
{
	printf( "%s\n"
		, m_message.c_str()
		);

	return true;
}